/**
 * op_add - entry point
 * Description: returns the sum of a and b
 * @a: integer
 * @b: integer
 * Return: the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - entry point
 * Description: returns the difference of a and b
 * @a: integer
 * @b: integer
 * Return: the difference of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - entry point
 * Description: returns the product of a and b
 * @a: integer
 * @b: integer
 * Return: the product of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - entry point
 * Description: returns the division of a by b
 * @a: integer
 * @b: integer
 * Return: the division of a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - entry point
 * Description: returns the sum remainder of a by b
 * @a: integer
 * @b: integer
 * Return: the remainder of a by b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
